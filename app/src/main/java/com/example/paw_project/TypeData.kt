package com.example.paw_project

fun main(){
    //byte
    val bytevalue: Byte = 10
    println("Byte: $bytevalue")

    //short
    val shortvalue: Short = 20
    println("Short: $shortvalue")

    //int
    val intvalue: Int = 30
    println("Int: $intvalue")

    //long
    val longvalue: Long = 40
    println("Long: $longvalue")

    //float
    val floatvalue: Float = 50.6f
    println("Long: $floatvalue")

    //double
    val doublevalue: Double = 40.3
    println("Long: $doublevalue")

    //char
    val charvalue: Char = 'R'
    println(charvalue)

    //bolean
    val boleanvalue: Boolean = true
    println("Bolean: $boleanvalue")

    //string
    var stringvalue: String = "Hello, world!"
    println("String: $stringvalue")

    //string
    stringvalue = "Hello, kotlin!"
    println("String: $stringvalue")
}